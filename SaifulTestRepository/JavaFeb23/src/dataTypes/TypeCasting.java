package dataTypes;

public class TypeCasting {

	public static void main(String[] args) {
		// type casting is the process of converting 
		//one data types into another data types 
		
		double myDouble = 56.98;
		System.out.println("Double value is :" + myDouble);// double value 
		// type casting inot int value 
		int myInt = (int)myDouble;
		System.out.println("Int value is :" + myInt);// ? 56 
	}

}
